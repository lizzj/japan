<?php

namespace Modules\Mall\Repositories\Eloquent\Member\Asset;

use App\Exceptions\Exception\ErrorException;
use App\Jobs\Auth\User\User\Change;
use App\Repositories\Interfaces\Auth\User\UserRepository;
use Modules\Mall\Entities\Member\Asset\Commission;
use Modules\Mall\Repositories\Interfaces\Member\Asset\CommissionRepository;
use Modules\Mall\Repositories\Presenters\Member\Asset\CommissionPresenter;
use Prettus\Repository\Eloquent\BaseRepository;

/**
 * Class VideoRepositoryEloquent.
 *
 * @package namespace App\Repositories\Eloquent;
 */
class CommissionRepositoryEloquent extends BaseRepository implements CommissionRepository
{
    /**
     * Specify Model class name
     *
     * @return string
     */
    public function model()
    {
        return Commission::class;
    }

    public function presenter()
    {
        return app(CommissionPresenter::class); // TODO: Change the autogenerated stub
    }

    /**
     * Boot up the repository, pushing criteria
     */
    public function boot()
    {

    }

    public function handleData($data, $relate_key)
    {
        switch ($relate_key) {
            case 1:
                $data['content'] = '后台增加,账户佣金 ' . $data['num'];
                $data['source'] = 'adminInc';
                break;
            case 2:
                $data['content'] = '后台核减,账户佣金 ' . $data['num'];
                $data['source'] = 'adminDec';
                break;
            case 3:
                $data['content'] = '开通会员,团队奖励 ' . $data['num'];
                $data['source'] = 'levelReward';
                break;
            case 4:
                $data['content'] = '在线充值,团队奖励 ' . $data['num'];
                $data['source'] = 'rechargeReward';
                break;
            case 5:
                $data['content'] = '申请提现成功,核减佣金' . $data['num'];
                $data['source'] = 'applyWithdrawal';
                break;
            case 6:
                $data['content'] = '申请提现失败,返还佣金' . $data['num'];
                $data['source'] = 'rejectWithdrawal';
                break;
            default:
                throw new ErrorException('ParameterException', arrayTransitionObject(['message' => '参数错误']));
        }
        return $data;
    }

    public function processLog($data, $relate_key)
    {
        $user = app(UserRepository::class);
        $userInfo = $user->find($data['user_id'])['data'];
        $log = [
            'user_id' => $data['user_id'],
            'num' => $data['num'],
            'before' => $userInfo['commission'],
            'type' => $data['type'],
            'relate' => $data['relate'] ?? null,
            'relate_id' => $data['relate_id'] ?? null,
            'after' => $this->processAfter($userInfo['commission'], $data['num'], $data['type']),
        ];
        Change::dispatch($data['user_id'], 'commission', $data['type'] === 'inc' ? $data['num'] : -$data['num']);
        $this->create($this->handleData($log, $relate_key));
    }

    public function processAfter($old, $num, $type)
    {
        if ($type === 'inc') {
            return $old + $num;
        }
        return $old - $num;
    }

    public function adminList($title, $array, $limit, $interval)
    {
        return $this->scopeQuery(function ($query) use ($title, $array, $interval) {
            return $query->whereRaw("(concat(source,content) like '%" . $title . "%')")->where($array)->whereBetween('created_at', $interval);
        })->orderBy('id', 'desc')->paginate($limit);
    }

    public function selfList($array, $interval, $limit)
    {
        return $this->scopeQuery(function ($query) use ($array, $interval) {
            return $query->where($array)->whereBetween('created_at', $interval);
        })->orderBy('id', 'desc')->paginate($limit);
    }
}
